package main

import "fmt"

func main() {
	var name string

	name = "Agus"
	fmt.Println(name)

	name = "Agus Amirudin"
	fmt.Println(name)

	// simplify
	age := 27
	fmt.Println(age)

	//	multiple variable
	var (
		firstName = "Agus"
		lastName  = "Amirudin"
	)

	fmt.Println(firstName)
	fmt.Println(lastName)
}
